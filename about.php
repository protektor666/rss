				<h2>About</h2>

				<p>At FiveFilters.org we focus on two things: developing software to make web articles more accessible, and engaging in media activism to promote non-corporate, independent media.</p>
				<p>Our software has <a href="https://play.google.com/store/apps/details?id=org.fivefilters.kindleit">thousands</a> of <a href="https://chrome.google.com/webstore/detail/push-to-kindle/pnaiinchjaonopoejhknmgjingcnaloc?hl=en">happy</a> users: avid readers, news enthusiasts, researchers, and software developers.</p>
				<p>Our media activism work includes <a href="https://blockads.fivefilters.org/acceptable.html">promoting ad blocking</a>, <a href="http://blog.fivefilters.org/post/166687761892/explore-independent-media">highlighting media bias</a>, and <a href="https://fivefilters.org/explore_independent_media.php">promoting independent media</a>.</p>

				<h2>Why 'Five Filters'?</h2>
				
				<p>Edward Herman and Noam Chomsky describe the media as businesses which sell audiences to advertisers. In their <a href="https://chomsky.info/consent01/">propaganda model</a> of the media they point to five 'filters' which determine what we read in the newspapers and see on the television. These filters produce a very narrow view of the world that is in line with government policy and business interests.</p>

				<p>In the book <a href="http://www.medialens.org/index.php?option=com_content&view=article&id=93&Itemid=51">Newspeak in the 21st Century</a> David Edwards and David Cromwell ask</p>
				
				<blockquote><p>Why on earth do we, the public, continue looking to corporate media like the Guardian and the Independent for honest analysis and credible solutions? If we can wake up to the irrationality of our own trust and expectations &mdash; if we can become dis-illusioned &mdash; we can do something different. We can begin to build our own media, our own sources of information, communication and analysis. This, perhaps more than ever before, is entirely possible. As a result, ideas and action can emerge that are uncompromised by the profit drive. And out of this, in turn, can come hope that is not just one more clever deceit.</p></blockquote>
	
				<p>While there is still a lot of work to be done, there is already plenty of uncompromised, non-corporate news and analysis on the web. The aim of this project is to encourage people to start exploring the world of non-corporate media. We have tried to make these sources of news more accessible by creating web applications allowing users to find, select and read stories in a variety of formats. We hope these applications will also be useful for authors and publishers to more easily distribute their work.</p>

				<h2>How it started</h2>

				<figure class="figure-right img-polaroid" style="margin-right: -150px;"><img src="images/plan.jpg" /></figure>
				<!--<img src="images/plan.jpg" class="img-polaroid pull-right" />-->
				
				<p>The project started off as a proposal to explore the five filters of the propaganda model in an interactive installation. After exploring several ideas, we decided to focus on the creation of an application which would scan a physical newspaper clipping and find related news stories using non-corporate news sources.</p>
	
				<p>A prototype was developed at <a href="https://medialab-prado.es">Medialab-Prado</a> in Madrid, Spain during the <a href="https://medialab-prado.es/interactivos">Interactivos</a> 09 workshop. The installation was available to see and use at Medialab-Prado. More information on how the project developed during the workshop is available on our <a href="https://web.archive.org/web/20100109064629/http://wiki.medialab-prado.es/index.php/The_Five_Filters">wiki page</a>.</p>
				
				<p>We have since created a version to accept input through the browser: <a href="explore_independent_media.php">Explore Independent Media</a>.</p>

